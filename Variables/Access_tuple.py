# Tuple string List
tuple_list = ("hi", "hello", "bye")

# displaying all elements in tuple list
print(tuple_list)

# accessing first element
print(tuple_list[0])

# accessing third element
print(tuple_list[2])

#to access negative index of tuples
print(tuple_list[-2])
print(tuple_list[-1])